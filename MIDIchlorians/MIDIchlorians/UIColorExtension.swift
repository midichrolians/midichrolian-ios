extension UIColor {
    public class var violet: UIColor {
        return UIColor(red: 0.98, green: 0.82, blue: 0.97, alpha: 1.0)
    }

    public class var indigo: UIColor {
        return UIColor(red: 0.87, green: 0.76, blue: 0.98, alpha: 1.0)
    }

    public class var blue: UIColor {
        return UIColor(red: 0.76, green: 0.87, blue: 0.98, alpha: 1.0)
    }

    public class var green: UIColor {
        return UIColor(red: 0.76, green: 0.98, blue: 0.86, alpha: 1.0)
    }

    public class var yellow: UIColor {
        return UIColor(red: 0.98, green: 0.97, blue: 0.69, alpha: 1.0)
    }

    public class var orange: UIColor {
        return UIColor(red: 0.98, green: 0.84, blue: 0.61, alpha: 1.0)
    }

    public class var red: UIColor {
        return UIColor(red: 0.98, green: 0.77, blue: 0.73, alpha: 1.0)
    }
}
